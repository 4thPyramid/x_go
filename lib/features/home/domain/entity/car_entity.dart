class CarEntity {
  final String id;
  final String name;
  final String year;
  final String price;
  final String image;
  final String typeId;
  final String typeName;
  final int brandId;
  final String brandName;

  const CarEntity({
    required this.id,
    required this.name,
    required this.year,
    required this.price,
    required this.image,
    required this.typeId,
    required this.typeName,
    required this.brandId,
    required this.brandName,
  });
}